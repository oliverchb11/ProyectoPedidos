export class Productos {
  productoId: number;
  nombreProducto: string;
  stock: number;
  precio: number;
  descripcion: string;

  constructor() {
    this.productoId = this.productoId;
    this.nombreProducto = this.nombreProducto;
    this.stock = this.stock;
    this.precio = this.precio;
    this.descripcion = this.descripcion;
  }
}
